public class Car
{
    private Long id;
    private String number;
    private int seats;

    public Long getId() {
        return id;
    }

    public String getNumber() {
        return number;
    }

    public int getSeats() {
        return seats;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public void setSeats(int seats) {
        this.seats = seats;
    }
}
